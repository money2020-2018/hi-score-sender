import React from 'react';
import {
  Image,
  Platform,
  ScrollView,
  StyleSheet,
  Text,
  Button,
  TouchableOpacity,
  View,
  Slider
} from 'react-native';
import { WebBrowser } from 'expo';

import { MonoText } from '../components/StyledText';

export default class HomeScreen extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      baseAllowance: 5
    };
  }

  change(baseAllowance) {
    this.setState(() => {
      return {
        baseAllowance: baseAllowance.toFixed(2)
      };
    });
  }

  render() {
    const {baseAllowance} = this.state;
    const {navigate} = this.props.navigation;

    return (
      <View style={styles.container}>
        <ScrollView style={styles.container} contentContainerStyle={styles.contentContainer}>
          <Text style={styles.getStartedText}>Set Allowance</Text>
          <Text style={styles.text}>{String(baseAllowance)}</Text>
          <Slider
            style={styles.sliderContainer}
            step={.05}
            maximumValue={8}
            minimumValue={3}
            onValueChange={this.change.bind(this)}
            value={baseAllowance}
          />

          <Button
            style={styles.sendButton}
            onPress={this._onPressLearnMore}
            title="Send"
            color="#fdbb0a"
            accessibilityLabel="Submit button to send allowance"
          />

        </ScrollView>
      </View>
    );
  }

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      const learnMoreButton = (
        <Text onPress={this._handleLearnMorePress} style={styles.helpLinkText}>
          Learn more
        </Text>
      );

      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled, your app will be slower but you can use useful development
          tools. {learnMoreButton}
        </Text>
      );
    } else {
      return (
        <Text style={styles.developmentModeText}>
          You are not in development mode, your app will run at full speed.
        </Text>
      );
    }
  }

  _onPressLearnMore = () => {
  };

  _handleLearnMorePress = () => {
    WebBrowser.openBrowserAsync('https://docs.expo.io/versions/latest/guides/development-mode');
  };

  _handleHelpPress = () => {
    WebBrowser.openBrowserAsync(
      'https://docs.expo.io/versions/latest/guides/up-and-running.html#can-t-see-your-changes'
    );
  };
}

